package com.zielonka.kz;

public class ProductStateBean {

	
	private String name;
	private String code;
	private int number;
	private String enume;
	private int kupka;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getEnume() {
		return enume;
	}
	public void setEnume(String enume) {
		this.enume = enume;
	}
	public int getKupka() {
		return kupka;
	}
	public void setKupka(int kupka) {
		this.kupka = kupka;
	}
	

}
