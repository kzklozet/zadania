package com.zielonka.kz;

public class ProductDts {

	private String name;
	private String code;
	private int number;
	private ProductEnum enume;
	private int bok;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public ProductEnum getEnume() {
		return enume;
	}
	public void setEnume(ProductEnum enume) {
		this.enume = enume;
	}
	public int getBok() {
		return bok;
	}
	public void setBok(int bok) {
		this.bok = bok;
	}
}
